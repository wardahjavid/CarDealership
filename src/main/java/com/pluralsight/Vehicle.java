package com.pluralsight;

public class Vehicle {
    private int vehicle;       // previously "vin"
    private int year;
    private String make;
    private String model;
    private String type;
    private String color;
    private int odometer;
    private double price;


    public Vehicle(int vehicle, int year, String make, String model,
                   String type, String color, int odometer, double price) {
        this.vehicle = vehicle;
        this.year = year;
        this.make = make;
        this.model = model;
        this.type = type;
        this.color = color;
        this.odometer = odometer;
        this.price = price;
    }

    public int getVehicle() {
        return vehicle;
    }

    public void setVehicle(int vehicle) {
        this.vehicle = vehicle;
    }

    public int getYear() {
        return year;
    }

    public void setYear(int year) {
        this.year = year;
    }

    public String getMake() {
        return make;
    }

    public void setMake(String make) {
        this.make = make;
    }

    public String getModel() {
        return model;
    }

    public void setModel(String model) {
        this.model = model;
    }

    public String getType() {
        return type;
    }

    public void setType(String type) {
        this.type = type;
    }

    public String getColor() {
        return color;
    }

    public void setColor(String color) {
        this.color = color;
    }

    public int getOdometer() {
        return odometer;
    }

    public void setOdometer(int odometer) {
        this.odometer = odometer;
    }

    public double getPrice() {
        return price;
    }

    public void setPrice(double price) {
        this.price = price;
    }

    @Override
    public String toString() {
        return String.format(
                "%d | %d | %s | %s | %s | %s | %d miles | $%.3f",
                vehicle, year, make, model, type, color, odometer, price
        );
    }
}
